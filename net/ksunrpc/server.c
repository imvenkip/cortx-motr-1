#define PROC(num, name, respsize)				\
 [num] = {							\
   .pc_func	= (svc_procfunc) ksunrpc_proc_##name,		\
   .pc_decode	= (kxdrproc_t) ksunrpc_decode_##name,		\
   .pc_encode	= (kxdrproc_t) ksunrpc_encode_##name##_resp,	\
